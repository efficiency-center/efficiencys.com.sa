export const SILHOUETTE_MASKS: Record<SilhouetteVariant, number[][]> = {
  "male": MASK_MALE,
  "female": MASK_FEMALE,
};

export const SILHOUETTE_VARIANTS = Object.keys(SILHOUETTE_MASKS) as SilhouetteVariant[];

export function maskDensity(variant: SilhouetteVariant, x: number, y: number, seed: number): number {
  if (y < 0 || y >= SILHOUETTE_ROWS || x < 0 || x >= SILHOUETTE_COLS) return 0;
  const base = SILHOUETTE_MASKS[variant][y][x];
  if (!base) return 0;
  const edgeNoise = ((seed + x * 13 + y * 19) % 11) / 55;
  return 0.85 + edgeNoise;
}

export type MaskBounds = {
  minX: number;
  maxX: number;
  minY: number;
  maxY: number;
  centerX: number;
  centerY: number;
};

export function getMaskBounds(variant: SilhouetteVariant): MaskBounds {
  const grid = SILHOUETTE_MASKS[variant];
  let minX = SILHOUETTE_COLS;
  let maxX = 0;
  let minY = SILHOUETTE_ROWS;
  let maxY = 0;

  for (let y = 0; y < SILHOUETTE_ROWS; y += 1) {
    for (let x = 0; x < SILHOUETTE_COLS; x += 1) {
      if (!grid[y][x]) continue;
      minX = Math.min(minX, x);
      maxX = Math.max(maxX, x);
      minY = Math.min(minY, y);
      maxY = Math.max(maxY, y);
    }
  }

  return {
    minX,
    maxX,
    minY,
    maxY,
    centerX: (minX + maxX) / 2,
    centerY: (minY + maxY) / 2,
  };
}
